public class Number3 {
    public static void main(String[] args) {
        double a, b;
        a = 9.3;
        b = 9.1;
        if (a == b) {
            System.out.println("a is equal to b");
        }
        else {
            if (b > a)
                System.out.println("b is bigger");
            else
                System.out.println("a is bigger");
        }
    }
}